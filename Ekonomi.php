								<h3>E SDM</h3>
								<p>Sumber daya mineral atau tambang yang ada di DIY adalah Bahan Galian C yang meliputi, pasir, kerikil, batu gamping, kalsit, kaolin, dan zeolin serta breksi batu apung. Selain bahan galian Golongan C tersebut, terdapat bahan galian Golongan A yang berupa Batu Bara. Batu bara ini sangat terbatas jumlahnya, begitu pula untuk bahan galian golongan B berupa Pasir Besi (Fe), Mangan (Mn), Barit (Ba), dan Emas (Au) yang terdapat di Kabupaten Kulon Progo. Dalam bidang ketenagalistrikan, khususnya listrik, minyak, dan gas di DIY dipasok oleh PT PLN dan PT Pertamina.</p>
