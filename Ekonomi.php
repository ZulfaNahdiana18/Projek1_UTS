								<h3>Pertanian dan Kehutanan</h3>
								<p>Tingkat kesejahteraan petani dalam bidang pertanian di DIY yang diukur dengan Nilai Tukar Petani (NTP) NTP dapat menjadi salah satu indikator yang menunjukkan tingkat kesejahteraan petani di suatu wilayah. Pada 2010 NTP sebesar 112,74%.Ketahanan pangan merupakan bagian terpenting dari pemenuhan hak atas pangan sekaligus merupakan salah satu pilar utama hak asasi manusia.</p>
								<p>Secara umum ketersediaan pangan di DIY cukup karena berkaitan dengan musim panen sehingga diperlukan pengaturan distribusi oleh pemerintah. Pemenuhan kebutuhan ikan di DIY dapat dipenuhi dari perikanan tangkap maupun budidaya. Untuk perikanan tangkap dilakukan melalui pengembangan pelabuhan perikanan Sadeng dan Glagah. Produksi perikanan budidaya tahun 2010 mencapai 39.032 ton, dan perikanan tangkap mencapai 4.906 ton, dengan konsumsi ikan sebesar 22,06 kg/kap/tahun.</p>
								<p>Hutan di DIY didominasi oleh hutan produksi, yang sebagian besar berada di wilayah Kabupaten Gunungkidul. Persentase luas hutan di DIY pada tahun 2010 sebesar 5,87% dengan rehabilitasi lahan kritis sebesar 9,93% dan kerusakan kawasan hutan sebesar 4,94%.</p>
								<p>Sektor perkebunan, dari segi produksi tanaman perkebunan yang potensial di DIY adalah kelapa, dan tebu. Kegiatan perkebunan diprioritaskan dalam rangka pengutuhan tanaman memenuhi skala ekonomi serta peningkatan produksi, produktivitas, dan mutu produk tanaman untuk meningkatkan pendapatan petani.</p>
