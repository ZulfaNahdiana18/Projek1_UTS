								<p>Varian produk ekspor DIY andalan meliputi produk olahan kulit, tekstil, dan kayu. Pakaian jadi tekstil dan mebel kayu merupakan produk yang mempunyai nilai ekspor tertinggi. Namun, secara umum ekspor ke mancanegara didominasi oleh produk-produk yang dihasilkan dengan nilai seni, dan kreatif tinggi yang padat karya (labor intensive).</p>
								<p>Program pembangunan dalam mengembangkan koperasi dan UKM di DIY, salah satunya adalah memberdayakan usaha mikro, dan kecil, dan menengah yang disinergikan dengan kebijakan program dari pemerintah pusat. Salah satu upaya pembinaan UKM adalah melalui kelompok (sentra) karena upaya ini lebih efektif, dan efisien, di samping itu dengan sentra akan banyak melibatkan usaha mikro, dan kecil. Pada 2010 tercatat koperasi aktif sebanyak 1.926 koperasi, dan UKM tercatat 13.998 unit usaha.</p></div>
