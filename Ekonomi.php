								<h3>Pariwisata</h3>
								<p>Pariwisata merupakan sektor utama bagi DIY. Banyaknya objek, dan daya tarik wisata di DIY telah menyerap kunjungan wisatawan, baik wisatawan mancanegara maupun wisatawan nusantara. Pada 2010 tercatat kunjungan wisatawan sebanyak 1.456.980 orang, dengan rincian 152.843 dari mancanegara, dan 1.304.137 orang dari nusantara.</p>
								<p>Bentuk wisata di DIY meliputi wisata MICE (Meeting, Incentive, Convention and Exhibition), wisata budaya, wisata alam, wisata minat khusus, dan berbagai fasilitas wisata lainnya, seperti resort, hotel, dan restoran. Tercatat ada 37 hotel berbintang, dan 1.011 hotel melati di seluruh DIY pada 2010. Adapun penyelenggaraan MICE sebanyak 4.509 kali per tahun atau sekitar 12 kali per hari.</p>
								<p>Keanekaragaman upacara keagamaan, dan budaya dari berbagai agama serta didukung oleh kreativitas seni, dan keramahtamahan masyarakat, membuat DIY mampu menciptakan produk-produk budaya, dan pariwisata yang menjanjikan. Pada tahun 2010 tedapat 91 desa wisata dengan 51 di antaranya yang layak dikunjungi. Tiga desa wisata di kabupaten Sleman hancur terkena erupsi gunung Merapi sedang 14 lainnya rusak ringan.Menurut Kepala Dinas Pariwisata Yogyakarta pada September 2014, angka kunjungan mencapai 2,4 juta wisatawan domestik dan 1,8 juta wisatawan manca negara.</p>
								<p>Secara geografis, DIY juga diuntungkan oleh jarak antara lokasi objek wisata yang terjangkau, dan mudah ditempuh. Sektor pariwisata sangat signifikan menjadi motor kegiatan perekonomian DIY yang secara umum bertumpu pada tiga sektor andalan yaitu: jasa-jasa; perdagangan, hotel, dan restoran; serta pertanian. Dalam hal ini pariwisata memberi efek pengganda (multiplier effect) yang nyata bagi sektor perdagangan disebabkan meningkatnya kunjungan wisatawan. Selain itu, penyerapan tenaga kerja, dan sumbangan terhadap perekonomian daerah sangat signifikan.</p>
