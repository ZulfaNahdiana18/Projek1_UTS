									<center><h3>Kependudukan dan tenaga kerja</h3></center>
									<center><img class="" src="img/jalan_malioboro.png" alt=""><br></br></center>
									<p align=justify>Laju pertumbuhan penduduk di DIY antara 2003-2007 sebanyak 135.915 jiwa atau kenaikan rata-rata pertahun sebesar 1,1%. Umur Harapan Hidup (UHH) penduduk di DIY menunjukkan kecenderungan yang meningkat dari 72,4 tahun pada tahun 2002 menjadi 72,9 tahun pada tahun 2005. Ditinjau dari sisi distribusi penduduk menurut usia, terlihat kecenderungan yang semakin meningkat pada penduduk usia di atas 60 tahun.</p>
									<p align=justify>Proporsi distribusi peduduk berdasarkan usia produktif memiliki akibat pada sektor tenaga kerja. Angkatan kerja di DIY pada 2010 sebesar 71,41%. Di sektor ekonomi yang menyerap tenaga kerja paling besar adalah sektor pertanian kemudian disusul sektor jasa-jasa lainnya. Sektor yang potensial dikembangkan yaitu sektor pariwisata, sektor perdagangan, dan industri terutama industri kecil menengah serta kerajinan. Pengangguran di DIY menjadi problematika sosial yang cukup serius karena karakter pengangguran DIY menyangkut sebagian tenaga-tenaga profesional dengan tingkat pendidikan tinggi.</p>
									<p align=justify>Salah satu cara untuk mengatasi masalah kependudukan, dan ketenagakerjaan adalah dengan mengadakan program transmigrasi. Pelaksanaan pemberangkatan transmigran asal DIY sampai pada tahun 2008 melalui program transmigrasi sejumlah 76.495 KK atau 274.926 jiwa. Ditinjau dari pola transmigrasi sudah mencerminkan partisipasi, dan keswadayaan masyarakat, melalui Transmigrasi Umum (TU), Transmigrasi Swakarsa Berbantuan (TSB) dan Transmigrasi Swakarsa Mandiri (TSM). Untuk pensebarannya sudah mencakup hampir seluruh provinsi. Rasio jumlah tansmigran swakarsa mandiri pada 2010 mencapai 20% dari total transmigran yang diberangkatkan.</p>
