								<center><img class="" src="img/uny.png" alt=""><br></br></center>
								<p align=justify>Penyebaran sekolah untuk jenjang SD/MI sampai Sekolah Menengah sudah merata, dan menjangkau seluruh wilayah sampai ke pelosok desa. Jumlah SD/MI yang ada di DIY pada tahun 2008 adalah sejumlah 2.035, SMP/MTs/SMP Terbuka sejumlah 529, dan SMA/MA/SMK sejumlah 381 sekolah negeri maupun swasta. Ketersediaan ruang belajar dapat dikatakan sudah memadai dengan rasio siswa per kelas untuk SD/MI: 22, SMP/MTs: 33, SMA/MA/SMK: 31. Sedangkan tingkat ketersediaan guru di DIY juga cukup memadai dengan rasio siswa per guru untuk SD/MI: 13, SMP/MTs: 11, SMA/MA/SMK: 9. Untuk tahun 2010 pembinaan guru jenjang SD/MI sebanyak 3.900 guru telah memenuhi kualifikasi dari total 24.093 guru. Jenjang SMP/MTs sebanyak 3.939 guru telah memenuhi kualifikasi dari total 12.971 guru. Dan untuk SMA/MA sebanyak 4.826 guru telah memenuhi kualifikasi dari total 15.067 guru.</p>
								<p align=justify>Para lulusan jenjang SD/MI pada umumnya dapat melanjutkan ke SMP/MTs, sejalan kebijakan Wajib Belajar Pendidikan Dasar 9 Tahun yang dicanangkan pemerintah. Pada tahun 2010, angka kelulusan SD/MI mencapai 96,47%, SMP/MTs mencapai 81,84% dan SMA/MA/SMK sebesar 88,98%. Sedangkan angka putus sekolah pada tahun yang sama sebesar 0,07% untuk SD/MI; 0,17% untuk SMP/MTs; dan 0,44% untuk SMA/MA/SMK. Sementara itu jumlah perguruan tinggi di DIY baik negeri, swasta maupun kedinasan seluruhnya sebanyak 136 institusi dengan rincian 21 universitas, 5 institut, 41 sekolah tinggi, 8 politeknik dan 61 akademi yang diasuh oleh 9.736 dosen.</p>
