								<center><h3>Kesejahteraan dan kesehatan</h3></center>
								<center><img class="" src="img/sardjito.png" alt=""><br></br></center>
								<p align=justify>Sebagai salah satu aspek yang penting dalam kehidupan, pembangunan kesehatan menjadi salah satu instrumen di dalam upaya peningkatan kesejahteraan masyarakat. Tahun 2007 jumlah keluarga miskin sebanyak 275.110 RTM dan menerima bantuan raskin dari pemerintah pusat (meningkat 27 persen dibanding periode tahun 2006 sebanyak 216.536 RTM). Penduduk DIY menurut tahapan kesejahteraan tercatat bahwa pada tahun 2007 kelompok pra sejahtera 21,12%; Sejahtera I 22,70%; Sejahtera II 23,69%; Sejahtera III 26,83%; dan Sejahtera III plus 5,66%. Tingkat kesejahteraan pada tahun 2010 meningkat dengan penurunan persentase penduduk miskin menjadi 16,83%.</p>
								<p align=justify>Arah pembangunan kesehatan di DIY secara umum adalah untuk mewujudkan DIY yang memiliki status kesehatan masyarakat yang tinggi tidak hanya dalam batas nasional tetapi memiliki kesetaraan di tataran internasional khususnya Asia Tenggara dengan mempertinggi kesadaran masyarakat akan pentingnya hidup sehat, peningkatan jangkauan, dan kualitas pelayanan kesehatan serta menjadikan DIY sebagai pusat mutu dalam pelayanan kesehatan, pendidikan pelatihan kesehatan serta konsultasi kesehatan. Hasil Riset Kesehatan Dasar Nasional Tahun 2010 menempatkan DIY sebagai daerah setingkat provinsi dengan indikator kesehatan terbaik, dan paling siap dalam mencapai MDGâ€™s.</p>
								<p align=justify>Pada tahun 2010 capaian indikator kesehatan untuk umur harapan hidup berada pada level usia 74,20 tahun. Angka kematian balita sebesar 18/1000 KH, angka kematian bayi sebesar 17/1000 KH, dan angka kematian ibu melahirkan sebesar 103/100.000 KH. Prevalensi gizi buruk sebesar 0.70%, Cakupan Rawat Jalan Puskesmas 16% sedangkan Cakupan Rawat Inap Rumah Sakit sebesar 1,32%.</p>
								<p align=justify>Dari 118 Puskesmas, 20% puskesmas telah menerapkan sistem manajemen mutu melalui pendekatan ISO 9001:200; 7% rumah sakit telah menerapkan ISO 9001:200; 25% rumah sakit di DIY telah terakreditasi dengan 5 standar; 17% RS terakreditasi dengan 12 standar; dan 5% RS telah terakreditasi dengan 16 standar pelayanan. Sarana pelayanan kesehatan yang memiliki unit pelayanan gawat darurat meningkat menjadi 40% dan RS dengan pelayanan kesehatan jiwa meningkat menjadi 9%. Meskipun demikian cakupan rawat jalan tahun 2006 baru mencapai 10% (nasional 15%) sementara untuk rawat inap 1,2% (nasional 1,5%). Rasio pelayanan kesehatan dasar bagi keluarga miskin secara cuma-cuma di Unit Pelaksana Teknis Dinas Kesehatan DIY maupun Kabupaten/Kota telah mencapai 100%. Rasio dokter umum per 100.000 penduduk menunjukkan tren meningkat sebesar 39,64 pada tahun 2006. Adapun program jamkesos tahun 2010 dianggarkan Rp. 34.978.592.000,00.</p>
								<p align=justify>Penyakit jantung dan stroke telah menjadi pembunuh nomor satu di DIY sementara faktor risiko penyakit jantung penduduk DIY ternyata cukup tinggi. Rumah tangga di DIY yang tidak bebas asap rokok sebesar 56%, sedangkan remaja yang perokok aktif sebesar 9,3%. Sebanyak 52% penduduk DIY kurang melakukan aktivitas olahraga, dan hanya 19,8% penduduk DIY yang mengkonsumsi serat mencukupi. Dalam tiga tahun terakhir angka obesitas pada anak-anak di DIY meningkat hampir 7%.</p>
