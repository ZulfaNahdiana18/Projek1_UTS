				<h2 class="pageTitle"><b>Sejarah</b></h2>
				<center><img class="" src="img/sejarah.png" alt=""><br></br></center>
				<p align=justify>Sebelum Indonesia merdeka, Yogyakarta merupakan daerah yang mempunyai pemerintahan sendiri atau disebut Zelfbestuurlandschappen/Daerah Swapraja, yaitu Kasultanan Ngayogyakarta Hadiningrat dan Kadipaten Pakualaman. Kasultanan Ngayogyakarta Hadiningrat didirikan oleh Pangeran Mangkubumi yang bergelar Sultan Hamengku Buwono I pada tahun 1755, sedangkan Kadipaten Pakualaman didirikan oleh Pangeran Natakusuma (saudara Sultan Hamengku Buwono II) yang bergelar Adipati Paku Alam I pada tahun 1813. Pemerintah Hindia Belanda mengakui Kasultanan, dan Pakualaman sebagai kerajaan dengan hak mengatur rumah tangganya sendiri yang dinyatakan dalam kontrak politik. Kontrak politik yang terakhir Kasultanan tercantum dalam Staatsblaad 1942 Nomor 47, sedangkan kontrak politik Pakualaman dalam Staatsblaad 1941 Nomor 577. Eksistensi kedua kerajaan tersebut telah mendapat pengakuan dari dunia internasional, baik pada masa penjajahan Belanda, Inggris, maupun Jepang. Ketika Jepang meninggalkan Indonesia, kedua kerajaan tersebut telah siap menjadi sebuah negara sendiri yang merdeka, lengkap dengan sistem pemerintahannya (susunan asli), wilayah, dan penduduknya.</p>
				<p align=justify>Setelah Proklamasi Kemerdekaan Republik Indonesia (RI), Sri Sultan Hamengkubuwana IX dan Sri Paku Alam VIII menyatakan kepada Presiden RI, bahwa Daerah Kasultanan Yogyakarta, dan Daerah Pakualaman menjadi wilayah Negara RI, bergabung menjadi satu kesatuan yang dinyatakan sebagai Daerah Istimewa Yogyakarta (DIY). Sri Sultan Hamengkubuwana IX dan Sri Paku Alam VIII sebagai Kepala Daerah, dan Wakil Kepala Daerah bertanggung jawab langsung kepada Presiden RI. Hal tersebut dinyatakan dalam:</p>
				<ol>
					<li>Piagam kedudukan Sri Sultan Hamengku Buwono IX dan Sri Paku Alam VIII tertanggal 19 Agustus 1945 dari Presiden RI.</li>
					<li>Amanat Sri Sultan Hamengku Buwono IX dan Sri Paku Alam VIII tertanggal 5 September 1945 (dibuat secara terpisah).</li>
					<li>Amanat Sri Sultan Hamengkubuwono IX dan Sri Paku Alam VIII tertanggal 30 Oktober 1945 (dibuat dalam satu naskah).</li>
				</ol>
				<p align=justify>Dalam perjalanan sejarah selanjutnya kedudukan DIY sebagai Daerah Otonom setingkat Provinsi sesuai dengan maksud pasal 18 Undang-undang Dasar 1945 (sebelum perubahan) diatur dengan Undang-undang Nomor 22 Tahun 1948 tentang Undang-undang Pokok Pemerintahan Daerah. Sebagai tindak lanjutnya kemudian Daerah Istimewa Yogyakarta dibentuk dengan Undang-undang Nomor 3 Tahun 1950 tentang Pembentukan Daerah Istimewa Yogyakarta Peraturan Pemerintah Nomor 31 Tahun 1950 sebagaimana telah diubah, dan ditambah terakhir dengan Undang-undang Nomor 9 Tahun 1955 (Lembaran Negara Tahun 1959 Nomor 71, Tambahan Lembaran Negara Nomor 1819) yang sampai saat ini masih berlaku. Dalam undang-undang tersebut dinyatakan DIY meliputi Daerah Kasultanan Ngayogyakarta Hadiningrat, dan Daerah Kadipaten Pakualaman. Pada setiap undang-undang yang mengatur Pemerintahan Daerah, dinyatakan keistimewaan DIY tetap diakui, sebagaimana dinyatakan terakhir dalam Undang-undang Nomor 32 Tahun 2004.</p>
				<p align=justify>Dalam sejarah perjuangan mempertahankan kemerdekaan Negara Kesatuan Republik Indonesia (NKRI), DIY mempunyai peranan yang penting. Terbukti pada tanggal 4 Januari 1946 sampai dengan tanggal 27 Desember 1949[8] pernah dijadikan sebagai ibu kota Indonesia. Tanggal 4 Januari inilah yang kemudian ditetapkan menjadi hari Yogyakarta Kota Republik pada tahun 2010. Pada saat ini Kasultanan Ngayogyakarta Hadiningrat dipimpin oleh Sri Sultan Hamengkubuwana X dan Kadipaten Pakualaman dipimpin oleh Sri Paku Alam X yang sekaligus menjabat sebagai Gubernur, dan Wakil Gubernur DIY. Keduanya memainkan peran yang menentukan dalam memelihara nilai-nilai budaya, dan adat istiadat Jawa dan merupakan pemersatu masyarakat Yogyakarta.</p>
				<p align=justify>Tahun 1965, seiring dengan gagalnya kudeta oleh G30S terhadap pemerintah nasional di Jakarta, Jawa Tengah dan banyak daerah lainnya, terjadilah penumpasan terhadap anggota dan simpatisan Partai Komunis Indonesia. Di Bali, diperkirakan lebih dari 100.000 orang terbunuh atau hilang. Meskipun demikian, kejadian-kejadian pada masa awal Orde Baru tersebut sampai dengan saat ini belum berhasil diungkapkan secara hukum.</p>
